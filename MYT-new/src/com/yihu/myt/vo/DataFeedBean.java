package com.yihu.myt.vo;

import java.io.Serializable;

public class DataFeedBean implements Serializable {
	private static final long serialVersionUID = 1L;

	public int Id;
	public String Subject;
	public String StartDate;
	public String EndDate;
	public int IsAllDayEvent;
	public int IsMoreThanOneDayEvent;
	public int RecurringEvent;
	public int Color;
	public int IsEditable;
	public String Location;
	public String Attendents;

	public int getId() {
		return Id;
	}

	public void setId(int id) {
		Id = id;
	}

	public String getSubject() {
		return Subject;
	}

	public void setSubject(String subject) {
		Subject = subject;
	}

	public String getStartDate() {
		return StartDate;
	}

	public void setStartDate(String startDate) {
		StartDate = startDate;
	}

	public String getEndDate() {
		return EndDate;
	}

	public void setEndDate(String endDate) {
		EndDate = endDate;
	}

	public int getIsAllDayEvent() {
		return IsAllDayEvent;
	}

	public void setIsAllDayEvent(int isAllDayEvent) {
		IsAllDayEvent = isAllDayEvent;
	}

	public int getIsMoreThanOneDayEvent() {
		return IsMoreThanOneDayEvent;
	}

	public void setIsMoreThanOneDayEvent(int isMoreThanOneDayEvent) {
		IsMoreThanOneDayEvent = isMoreThanOneDayEvent;
	}

	public int getRecurringEvent() {
		return RecurringEvent;
	}

	public void setRecurringEvent(int recurringEvent) {
		RecurringEvent = recurringEvent;
	}

	public int getColor() {
		return Color;
	}

	public void setColor(int color) {
		Color = color;
	}

	public int getIsEditable() {
		return IsEditable;
	}

	public void setIsEditable(int isEditable) {
		IsEditable = isEditable;
	}

	public String getLocation() {
		return Location;
	}

	public void setLocation(String location) {
		Location = location;
	}

	public String getAttendents() {
		return Attendents;
	}

	public void setAttendents(String attendents) {
		Attendents = attendents;
	}

	public static long getSerialversionuid() {
		return serialVersionUID;
	}
	
}
